package org.tnsindia.autounboxingdemo;
//demo on un-boxing
public class UnboxingDemo {

	public static void main(String[] args) {
		
		Integer x=50;
		//converting object type to primitive
		int i=x;
		System.out.println(i);
		System.out.println(x.getClass().getName());
		  
		
	}

}
