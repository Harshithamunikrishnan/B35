package org.tnsindia.collectiondemo;

public class Student {
	//private data members
	private int marks;
	private String name;
	
	//getters and setters
	public int getMarks() {
		return marks;
	}
	public void setMarks(int marks) {
		this.marks = marks;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
		
}
